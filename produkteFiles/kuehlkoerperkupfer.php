Hierfür bieten sich vor allem gut wärmeleitende Metalle wie Kupfer oder Aluminium an.
Wir verwenden die Kupfer-Kühlkörper von Rydges. <br \>
Montiert werden sie durch ein wärmeleitendes Klebeband, mit dem sie einfach direkt auf den Prozessor und den RAM geklebt werden können. <br \>
